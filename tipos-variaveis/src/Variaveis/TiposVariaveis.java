package Variaveis;
public class TiposVariaveis {
    public static void main(String[] args){
        String meuNome = "Jaderson Teles";
        double salarioMimino = 2500.33;
        System.out.println("Salario de " +meuNome + " é " +salarioMimino);

        // Definir uma variavel contante que nunca poderá ser mudada: use final (Declare a varivel em caixa alta ).
        final double VALOR_DE_PI = 3.14;
        System.out.println("VALOR DE pi "+ VALOR_DE_PI);

        // operadores
        String nomeCompleto = "Linguagem " + "Java";
        System.out.println(nomeCompleto);
    }
}
